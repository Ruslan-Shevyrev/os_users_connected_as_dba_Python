SELECT UNIFIED_AUDIT_POLICIES,
		OS_USERNAME,
		USERHOST, 
		CLIENT_PROGRAM_NAME,
		CURRENT_USER,  
		COUNT(*) AS RECORDS,
		TRUNC(EVENT_TIMESTAMP_UTC) AS CONNECT_DATE
	FROM UNIFIED_AUDIT_TRAIL
	WHERE EVENT_TIMESTAMP_UTC BETWEEN TRUNC(SYSDATE) - 1 AND SYSDATE
		AND ACTION_NAME = 'LOGON'
		AND CURRENT_USER IN ('SYS', 'SYSTEM')
	GROUP BY UNIFIED_AUDIT_POLICIES,
			OS_USERNAME,
			USERHOST, 
			CLIENT_PROGRAM_NAME,
			CURRENT_USER,
			TRUNC(EVENT_TIMESTAMP_UTC)